package yh.subsys.infomgr.bilingual.data;

public class YHPostulantInfo {
  private int seqId;
  private String languageKind;
  private String languageLevel;
  private int amount;
  private String postulantId;
  
  public int getSeqId() {
    return seqId;
  }
  public void setSeqId(int seqId) {
    this.seqId = seqId;
  }
  public String getLanguageKind() {
    return languageKind;
  }
  public void setLanguageKind(String languageKind) {
    this.languageKind = languageKind;
  }
  public String getLanguageLevel() {
    return languageLevel;
  }
  public void setLanguageLevel(String languageLevel) {
    this.languageLevel = languageLevel;
  }
  public int getAmount() {
    return amount;
  }
  public void setAmount(int amount) {
    this.amount = amount;
  }
  public String getPostulantId() {
    return postulantId;
  }
  public void setPostulantId(String postulantId) {
    this.postulantId = postulantId;
  }

 
}
