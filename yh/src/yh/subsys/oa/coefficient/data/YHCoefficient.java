package yh.subsys.oa.coefficient.data;

public class YHCoefficient {
	private int seqId;
	private double yearScore;
	private double monthScore;
	private double chiefScore;
	private double checkScore;
	private double awardScore;

	public int getSeqId() {
		return seqId;
	}

	public void setSeqId(int seqId) {
		this.seqId = seqId;
	}

	public double getYearScore() {
		return yearScore;
	}

	public void setYearScore(double yearScore) {
		this.yearScore = yearScore;
	}

	public double getMonthScore() {
		return monthScore;
	}

	public void setMonthScore(double monthScore) {
		this.monthScore = monthScore;
	}

	public double getChiefScore() {
		return chiefScore;
	}

	public void setChiefScore(double chiefScore) {
		this.chiefScore = chiefScore;
	}

	public double getCheckScore() {
		return checkScore;
	}

	public void setCheckScore(double checkScore) {
		this.checkScore = checkScore;
	}

	public double getAwardScore() {
		return awardScore;
	}

	public void setAwardScore(double awardScore) {
		this.awardScore = awardScore;
	}

}
