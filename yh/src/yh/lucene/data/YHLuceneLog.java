package yh.lucene.data;

public class YHLuceneLog {
    private int seqId;
    private String luTime;
    private String luUser;
    private String luStr;
    public int getSeqId() {
      return seqId;
    }
    public void setSeqId(int seqId) {
      this.seqId = seqId;
    }
    public String getLuTime() {
      return luTime;
    }
    public void setLuTime(String luTime) {
      this.luTime = luTime;
    }
    public String getLuUser() {
      return luUser;
    }
    public void setLuUser(String luUser) {
      this.luUser = luUser;
    }
    public String getLuStr() {
      return luStr;
    }
    public void setLuStr(String luStr) {
      this.luStr = luStr;
    }
    
    
}
