create table DIARY(
  SEQ_ID INT PRIMARY KEY,
  USER_ID INT NOT NULL,
  DIA_TIME DATE NOT NULL,
  DIA_TYPE CHAR(1) DEFAULT '1',
  SUBJECT VARCHAR2(200) NOT NULL,
  CONTENT CLOB,
  ATTACHMENT_ID VARCHAR2(2000),
  ATTACHMENT_NAME VARCHAR2(2000),
  LAST_COMMENT_TIME TIMESTAMP,
  TO_ID VARCHAR2(2000),
  COMPRESS_CONTENT CLOB,
  READERS VARCHAR2(2000)
);
exec pr_createidentitycolumn('DIARY','SEQ_ID');

create table DIARY_COMMENT(
  SEQ_ID INT PRIMARY KEY,
  DIA_ID INT NOT NULL,
  USER_ID INT NOT NULL,
  CONTENT CLOB,
  SEND_TIME TIMESTAMP,
  COMMENT_FLAG CHAR(1) DEFAULT '0'
);
exec pr_createidentitycolumn('DIARY_COMMENT','SEQ_ID');
create table DIARY_COMMENT_REPLY(
  SEQ_ID INT PRIMARY KEY,
  REPLY_COMMENT CLOB,
  REPLY_TIME TIMESTAMP,
  REPLYER VARCHAR2(500),
  COMMENT_ID INT NOT NULL
);
exec pr_createidentitycolumn('DIARY_COMMENT_REPLY','SEQ_ID');