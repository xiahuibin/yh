package yh.core.funcs.portal.data;

public class YHPortContainer {
  private int seqId;
  private String type;
  private int parts;
  private int width;
  
  public int getSeqId() {
    return seqId;
  }
  public void setSeqId(int seqId) {
    this.seqId = seqId;
  }
  public String getType() {
    return type;
  }
  public void setType(String type) {
    this.type = type;
  }
  public int getParts() {
    return parts;
  }
  public void setParts(int parts) {
    this.parts = parts;
  }
  public int getWidth() {
    return width;
  }
  public void setWidth(int width) {
    this.width = width;
  }
}
